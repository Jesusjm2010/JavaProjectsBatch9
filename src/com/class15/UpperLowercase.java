package com.class15;

public class UpperLowercase {

	public static void main(String[] args) {
		
		String var1="Hello";
		System.out.println(var1.toLowerCase());
		System.out.println(var1.toUpperCase());
		var1="Hello 123";
		System.out.println(var1.toLowerCase());
		System.out.println(var1);
	}

}
